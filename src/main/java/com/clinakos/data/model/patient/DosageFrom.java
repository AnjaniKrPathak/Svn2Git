package com.clinakos.data.model.patient;

public class DosageFrom {
	private int id;
	private String dosageFrom;
	private int newCropId;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public String getDosageFrom() {
		return dosageFrom;
	}
	public void setDosageFrom(String dosageFrom) {
		this.dosageFrom = dosageFrom;
	}
	public int getNewCropId() {
		return newCropId;
	}
	public void setNewCropId(int newCropId) {
		this.newCropId = newCropId;
	}
	
	
}
