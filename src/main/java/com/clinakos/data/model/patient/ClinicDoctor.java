package com.clinakos.data.model.patient;

public class ClinicDoctor {
	private int clinicDoctorid;
	private int doctorId;
	private int clinicProviderID;
	public int getClinicDoctorid() {
		return clinicDoctorid;
	}
	public void setClinicDoctorid(int clinicDoctorid) {
		this.clinicDoctorid = clinicDoctorid;
	}
	public int getDoctorId() {
		return doctorId;
	}
	public void setDoctorId(int doctorId) {
		this.doctorId = doctorId;
	}
	public int getClinicProviderID() {
		return clinicProviderID;
	}
	public void setClinicProviderID(int clinicProviderID) {
		this.clinicProviderID = clinicProviderID;
	}
	
	
	
	

}
