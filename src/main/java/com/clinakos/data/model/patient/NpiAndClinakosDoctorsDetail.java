public class NpiAndClinakosDoctorsDetail {
	private int doctorId;
	private String firstName;
	private String middleName;
	private String lastName;
	private String npiNumber;
	private String upinNumber;
	private String phoneNumber;
	private int providerId;
	private boolean clinakosUser;
	
	
	
	/**
	 * @return the doctorId
	 */
	public int getDoctorId() {
		return doctorId;
	}
	/**
	 * @param doctorId the doctorId to set
	 */
	public void setDoctorId(int doctorId) {
		this.doctorId = doctorId;
	}
	/**
	 * @return the firstName
	 */
	public String getFirstName() {
		return firstName;
	}
	/**
	 * @param firstName the firstName to set
	 */
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	/**
	 * @return the middleName
	 */
	public String getMiddleName() {
		return middleName;
	}
	/**
	 * @param middleName the middleName to set
	 */
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	/**
	 * @return the lastName
	 */
	public String getLastName() {
		return lastName;
	}
	/**
	 * @param lastName the lastName to set
	 */
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	/**
	 * @return the npiNumber
	 */
	public String getNpiNumber() {
		return npiNumber;
	}
	/**
	 * @param npiNumber the npiNumber to set
	 */
	public void setNpiNumber(String npiNumber) {
		this.npiNumber = npiNumber;
	}
	/**
	 * @return the upinNumber
	 */
	public String getUpinNumber() {
		return upinNumber;
	}
	/**
	 * @param upinNumber the upinNumber to set
	 */
	public void setUpinNumber(String upinNumber) {
		this.upinNumber = upinNumber;
	}
	/**
	 * @return the phoneNumber
	 */
	public String getPhoneNumber() {
		return phoneNumber;
	}
	/**
	 * @param phoneNumber the phoneNumber to set
	 */
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	/**
	 * @return the providerId
	 */
	public int getProviderId() {
		return providerId;
	}
	/**
	 * @param providerId the providerId to set
	 */
	public void setProviderId(int providerId) {
		this.providerId = providerId;
	}
	/**
	 * @return the clinakosUser
	 */
	public boolean isClinakosUser() {
		return clinakosUser;
	}
	/**
	 * @param clinakosUser the clinakosUser to set
	 */
	public void setClinakosUser(boolean clinakosUser) {
		this.clinakosUser = clinakosUser;
	}
	
	
	
}
