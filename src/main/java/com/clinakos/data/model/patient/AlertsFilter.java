package com.clinakos.data.model.patient;

public class AlertsFilter {

	private String itemLabelStr;
	private String itemValueStr;
	private String imageValueStr;
	public String getItemLabelStr() {
		return itemLabelStr;
	}
	public void setItemLabelStr(String itemLabelStr) {
		this.itemLabelStr = itemLabelStr;
	}
	public String getItemValueStr() {
		return itemValueStr;
	}
	public void setItemValueStr(String itemValueStr) {
		this.itemValueStr = itemValueStr;
	}
	public String getImageValueStr() {
		return imageValueStr;
	}
	public void setImageValueStr(String imageValueStr) {
		this.imageValueStr = imageValueStr;
	}
	
	
}
